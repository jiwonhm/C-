#pragma once
#include <iostream>
#include "operations.h"

namespace cal {
	
		double addition::add(double a, double b) {
			return a + b;
		}

	
		double subtraction::sub(double a, double b) {
			return a - b;
		}


	
		double multiplication::multi(double a, double b) {
			return a * b;
		}


	
		double division::div(double a, double b) {
			return a / b;
		}


}
